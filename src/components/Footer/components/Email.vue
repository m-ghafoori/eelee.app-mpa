    basicColor: {
      default: "#0d8a6f",
    },
    gradientColor: {
      default: "unset",
    },
  },

  data() {
    return {
      surroundingColor: String,
      centralColor: String,
    };
  },

  watch: {
    basicColor(val) {
      this.surroundingColor = val;
      if (this.gradientColor == "unset") this.centralColor = val;
    },
    gradientColor(val) {
      if (this.gradientColor != "unset") this.centralColor = val;
    },
  },

  mounted() {
    this.surroundingColor = this.basicColor;
    this.gradientColor == "unset"
      ? (this.centralColor = this.basicColor)
